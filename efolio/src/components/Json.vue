      <p>Our <code>authors.json</code> contains an array of author objects.</p>

      <h3>Iterating through Arrays</h3>
      <!-- Activity 6: Render a list containing author names and their birth years. -->
      <ul>
        <li
          v-for="a in authors"
          :key="a.id"
          :title="`Author: ${a.name} ‚Ä¢ Born: ${a.birthYear}`"
          <!--
          attribute
          binding
          --
        >
          :class="{ highlight: a.name === 'George Orwell' }"
          <!-- class binding -->
          :style="a.name === 'George Orwell' ? /* style binding */ {
          backgroundColor: '#fff3cd', border: '1px solid #f0ad4e' } : {}" >
          <strong>{{ a.name }}</strong> ({{ a.birthYear }})
        </li>
      </ul>

      <h3>Filtering Arrays</h3>
      <!-- Activity 7: authors born after 1850 -->
      <p>Authors born after 1850:</p>
      <ul>
        <li v-for="a in modernAuthors" :key="a.id">
          {{ a.name }} ({{ a.birthYear }})
        </li>
      </ul>

      <h3>Mapping Arrays</h3>
      <p>Famous works:</p>
      <ul>
        <!-- Activity 8: list all famous work titles -->
        <li v-for="(t, i) in allFamousWorks" :key="i">{{ t }}</li>
      </ul>

      <h3>Finding in Arrays</h3>
      <p>Finding by property: {{ orwell?.name }}</p>

      <h3>Nested Arrays/Objects</h3>
      <p>{{ austen?.name }}'s works:</p>
      <!-- Activity 9: Austen's works -->
      <ul>
        <li v-for="(w, i) in austen?.famousWorks || []" :key="i">
          {{ w.title }} ({{ w.year }})
        </li>
      </ul>
    </section>

    <section class="lab-section">
      <h2>üè¢ Working with JSON Objects</h2>
      <p>Our <code>bookstores.json</code> is a JSON object.</p>

      <h3>Accessing Properties</h3>
      <p>
        Company:
        <!-- Activity 9a -->
        <strong>{{ bookstore.name }}</strong>
      </p>

      <p>
        Total Stores:
        <!-- Activity 9b -->
        <strong>{{ bookstore.totalStores }}</strong>
